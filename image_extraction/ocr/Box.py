class Box(object):
    """
    A class that represents a box on the image of the homework organizer.

    Examples: box for name of student, box for subject of student, box for individual scores on homework, e.t.c.

    """

    def __int__(self, x, y, w, h):
        self.x = x
        self.y = y
        self.w = w
        self.h = h



